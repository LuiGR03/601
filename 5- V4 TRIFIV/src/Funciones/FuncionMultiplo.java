
package Funciones;


public class FuncionMultiplo {
    
    public String returnaMultiplo(int num){
        
        if(num == 0 && num == 0){
                    return("TRIFIV");
                    
                } else if(num == 0){
                    return("TRI");
                    
                }else if(num == 0){
                    return("FIV");
                }
                return "Ningun valor";
    }
        
    }
